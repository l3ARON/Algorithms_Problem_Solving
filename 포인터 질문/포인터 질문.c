/*
좋은 질문입니다! markBingo 함수에서 bingo 배열의 내용을 수정하면 그 변경 사항이 main 함수에도 반영되는 이유는 C 언어에서 배열이 함수에 전달될 때 포인터로 전달되기 때문입니다.

배열과 포인터
C 언어에서 배열은 그 자체로 포인터로 취급됩니다. 따라서, 배열을 함수에 전달하면 배열의 첫 번째 요소의 주소가 전달됩니다. 이는 함수가 배열의 복사본을 받는 것이 아니라, 원래 배열의 메모리 주소를 받기 때문에 함수 내에서 배열의 내용을 수정하면 원본 배열에도 그 수정이 반영됩니다.

예시
main 함수에서 bingo 배열을 선언하고, 그 주소를 markBingo 함수에 전달합니다.
markBingo 함수는 전달받은 배열의 메모리 주소를 사용하여 직접 배열의 값을 변경합니다.
이 변경은 원본 배열 bingo에 반영됩니다.
*/

#include <stdio.h>

void modifyArray(int arr[], int size) {
    arr[0] = 10; // 배열의 첫 번째 요소를 10으로 수정
}

int main() {
    int myArray[5] = {1, 2, 3, 4, 5}; // 배열 선언

    modifyArray(myArray, 5); // 배열을 함수에 전달

    printf("%d\n", myArray[0]); // 첫 번째 요소 출력

    return 0;
}

/*
위의 코드에서 modifyArray 함수는 myArray 배열의 첫 번째 요소를 10으로 수정합니다. main 함수에서 myArray[0]을 출력하면 10이 출력됩니다. 이는 modifyArray 함수에서 배열의 복사본이 아닌, 원본 배열을 직접 수정했기 때문입니다.

markBingo 함수에서의 적용
markBingo 함수도 마찬가지로 배열을 수정하는 작업을 수행합니다.

여기서 b 배열은 bingo 배열의 주소를 가지고 있습니다. 따라서 b[row][col] = 1; 이라는 명령어는 실제로 bingo 배열의 값을 수정하게 됩니다.

결론
함수에 배열을 전달할 때, 배열의 포인터가 전달되므로 함수에서 배열의 값을 수정하면, 그 수정 사항은 원본 배열에 반영됩니다. 따라서 markBingo 함수에서 빙고 매트릭스의 값을 수정하면 main 함수에서 그 변경 사항이 적용됩니다.
*/
